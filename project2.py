def largest_cc_size(ugraph):
	
	"""
	Takes the undirected graph ugraph 
	and returns the size (an integer) 
	of the largest connected component in ugraph.
	"""

	cc_max = 0
	#cc_max_set = None
	cc_list = cc_visited(ugraph)
	for cc_set in cc_list:

		len_set = len(cc_set)
		if(len_set > cc_max):
			cc_max = len_set
			#cc_max_set = cc_set
	return cc_max

